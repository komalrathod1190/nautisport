						                    $(startTab.parentNode).next()[0];

						  if (!adjacentTab) {
						    var allTabs = $list.find('li');
						    if (dir === 'prev') {
						      adjacentTab = allTabs[allTabs.length - 1];
						    } else {
						      adjacentTab = allTabs[0];
						    }
						  }

